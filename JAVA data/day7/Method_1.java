package day7;

public class Method_1 {

	public static void main(String[] args) {
		printHelloWorld();
		printsong();
	}

	// parameter(인수)return값이 없는 메소드
	// HelloWorld가 출력되는 기능
	static void printHelloWorld() {
		System.out.println("HelloWorld");
	}

	// parameter(인수)return값이 없는 메소드
	// 애국가를 출력하는 기능
	static void printsong() {
		System.out.println("동해물과백두산이....");
	}

	// parameter(인수)있고 return값이 없는 메소드
	
	
	
}
